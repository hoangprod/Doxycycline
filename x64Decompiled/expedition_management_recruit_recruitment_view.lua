  local drawWnd = CreateSubOptionWindow(id, parent)
  drawWnd:Show(false)
  drawWnd:SetExtent(localeView.roaddMapOption.wnd.width, localeView.roaddMapOption.wnd.height)
  drawWnd:AddAnchor("TOPRIGHT", parent, "BOTTOMRIGHT", 3, 3)
  drawWnd.btn = {}
end
